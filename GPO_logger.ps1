$backupFilePath = "C:\registry_backup.reg"
reg export "HKLM" $backupFilePath /y
reg export "HKCU" $backupFilePath /y

$backupLocation = "C:\"
if (-not (Test-Path -Path $backupLocation)) {
    New-Item -ItemType Directory -Path $backupLocation
}
$gpos = Get-GPO -All
foreach ($gpo in $gpos) {
    Backup-GPO -Guid $gpo.Id -Path $backupLocation
    Write-Host "Backed up GPO: $($gpo.DisplayName)"
}
# Restore a GPO from backup
#Import-GPO -Path "C:\path\to\your\backup\Your GPO Name" -TargetName "Your GPO Name"

$ON = Read-Host -Prompt "Please enter your OU name"
Write-Host "You entered: $ON"
$DN = Read-Host -Prompt "Please enter your Domain name"
Write-Host "You entered: $DN"

$GPOName = "Audit Policy GPO"
$GPO = New-GPO -Name $GPOName
$AuditPolicySettings = Get-GPOAuditPolicy -All
foreach ($Setting in $AuditPolicySettings) {
    Set-GPAuditPolicy -Name $GPO.DisplayName -PolicySetting $Setting.PolicySetting -SuccessAudit $Setting.SuccessAudit -FailureAudit $Setting.FailureAudit
}
$OU = "OU=$ON,DC=$DN,DC=local"
New-GPLink -Name $GPO.DisplayName -Target $OU -LinkEnabled Yes